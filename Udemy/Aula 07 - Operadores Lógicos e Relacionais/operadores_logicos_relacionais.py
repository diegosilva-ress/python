"""
Operadores lógicos em Python são usados para combinar ou inverter condições,
retornando um valor booleano: True ou False.

Os principais operadores lógicos são:

and -> retorna True somente se todas as condições forem verdadeiras
or  -> retorna True se pelo menos uma condição for verdadeira
not -> inverte o resultado da condição, transformando True em False
       e False em True

Eles são muito usados em estruturas condicionais, como if, para tomar decisões
com base em mais de uma condição.

Operadores relacionais, também chamados de operadores de comparação,
são usados para comparar dois valores em Python.

Eles sempre retornam um valor booleano: True ou False.

Principais operadores de comparação:

>  maior que
<  menor que
== igual a
!= diferente de
>= maior ou igual a
<= menor ou igual a

Observação:
Em Python, o operador de diferente é !=.
O símbolo ! sozinho não é usado como operador lógico em Python.

O operador is verifica se duas variáveis apontam para o mesmo objeto
enquanto que o operador == verifica se o valor de duas variáveis são iguais.

"""

print(10 > 5)
print(10 < 5)
print(10 == 10)
print(10 != 5)
print(10 >= 10)
print(5 <= 3)